// Dynamic date utility functions
const now = new Date();
const today = new Date(now.getFullYear(), now.getMonth(), now.getDate());
const yesterday = new Date(today.getTime() - 24 * 60 * 60 * 1000);
const dayBeforeYesterday = new Date(today.getTime() - 48 * 60 * 60 * 1000);

// Specific historical dates for content sampling
const september20_2024 = new Date('2024-09-20T00:00:00.000Z');
const september19_2024 = new Date('2024-09-19T00:00:00.000Z');
const september18_2024 = new Date('2024-09-18T00:00:00.000Z');

const getRandomTimeInDay = (baseDate: Date, maxHours: number = 24): Date => {
  const randomHours = Math.random() * maxHours;
  const randomMinutes = Math.random() * 60;
  return new Date(baseDate.getTime() + (randomHours * 60 * 60 * 1000) + (randomMinutes * 60 * 1000));
};

// Enhanced date distribution: more articles for today and yesterday
const getIngestionDate = (index: number): Date => {
  // Distribute articles: 50% today, 30% yesterday, 20% day before yesterday
  const random = Math.random();
  if (random < 0.5) return getRandomTimeInDay(today);
  if (random < 0.8) return getRandomTimeInDay(yesterday);
  return getRandomTimeInDay(dayBeforeYesterday);
};

const getPublishDate = (): Date => {
  const twentyFourHoursAgo = new Date(now.getTime() - 24 * 60 * 60 * 1000);
  return getRandomTimeInDay(twentyFourHoursAgo, 24);
};

// Convert old score (1-10) to new tier (1-5)
const convertToTier = (oldScore: number): number => {
  if (oldScore >= 9) return 5; // Prime
  if (oldScore >= 7) return 4; // Ready
  if (oldScore >= 5) return 3; // Low Risk
  if (oldScore >= 3) return 2; // Moderate Risk
  return 1; // High Risk
};

// Generate articles with dynamic dates
const generateArticle = (base: any, index: number) => {
  const ingestionDate = getIngestionDate(index);
  return {
    ...base,
    ingestionTime: ingestionDate.toISOString(),
    publishDate: base.status === 'published' 
      ? getPublishDate().toISOString().split('T')[0]
      : ingestionDate.toISOString().split('T')[0],
    safetyScore: convertToTier(base.safetyScore) // Convert to tier system
  };
};

